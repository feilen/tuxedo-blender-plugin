class TestAddon(unittest.TestCase):
    def test_shapekey_button(self):
        test_name = bpy.path.basename(bpy.context.blend_data.filepath)
        result = bpy.ops.ft.create_shapekeys()

        # Confirm that the expected image result is randomly sampled
        self.assertTrue(test_name in sampling_lookup)
        all_keys = bpy.data.objects[bpy.context.scene.ft_mesh].data.shape_keys.key_blocks
        for (SRanipalKey, coords) in sampling_lookup[test_name].items():
            self.assertTrue(SRanipalKey in all_keys, SRanipalKey)
            selected_key = all_keys[SRanipalKey]
            for (v_index, coord) in coords.items():
                generic_coord = [p for p in selected_key.data[v_index].co]
                self.assertEqual(generic_coord, coord)

        test_object_names = {
            'facetracking.base.blend': [
                'Armature',
                'Cube',
            ],
        }
        self.assertEqual([o.name for o in bpy.data.objects], test_object_names[test_name])
        self.assertTrue(result == {'FINISHED'})

